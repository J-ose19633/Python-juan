# La estructura `if` se utiliza para ejecutar un bloque de código solo si una condición es verdadera.

# Ejemplo 1: Condición simple con un número
numero = 10

if numero > 0:
    print("El número es positivo.")

# Ejemplo 2: Condición simple con una cadena de texto
nombre = "María"

if nombre == "María":
    print(f"Hola, {nombre}!")

# Ejemplo 3: Condición simple con un booleano
esta_conectado = True

if esta_conectado:
    print("El usuario está en línea.")

