# Las variables son contenedores para almacenar valores de datos.
# En Python, no necesitas declarar el tipo de la variable, se infiere automáticamente.

# Ejemplo 1: Declaración y asignación de variables de diferentes tipos
nombre = "Jose"     # Tipo string
edad = 19            # Tipo entero (integer)
altura = 1.65        # Tipo flotante (float)
es_estudiante = True  # Tipo booleano (boolean)

print(f"Nombre: {nombre}, Edad: {edad}, Altura: {altura}, Estudiante: {es_estudiante}")

# Ejemplo 2: Reasignación de valores a variables
temperatura = 15
print(f"Temperatura inicial: {temperatura}°C")

temperatura = 28.5 # El valor de la variable se actualiza
print(f"Temperatura actualizada: {temperatura}°C")

