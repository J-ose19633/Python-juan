# Python tiene varios tipos de datos incorporados para manejar diferentes tipos de información.

# Ejemplo 1: Tipos Numéricos (int, float, complex)
entero = 10
flotante = 20.5
complejo = 1 + 2j

print(f"Entero: {entero}, Tipo: {type(entero)}")
print(f"Flotante: {flotante}, Tipo: {type(flotante)}")
print(f"Complejo: {complejo}, Tipo: {type(complejo)}")

# Ejemplo 2: Tipo de Texto (str)
cadena_simple = "Hola Python"
cadena_doble = 'Adiós Mundo'
cadena_multi_linea = """Esta es
una cadena
de varias líneas."""

print(f"Cadena simple: {cadena_simple}, Tipo: {type(cadena_simple)}")
print(f"Cadena doble: {cadena_doble}, Tipo: {type(cadena_doble)}")
print(f"Cadena multilínea: {cadena_multi_linea}, Tipo: {type(cadena_multi_linea)}")

# Ejemplo 3: Tipo Booleano (bool)
# Representa valores de verdad: True o False.
es_verdadero = True
es_falso = False

print(f"Es verdadero: {es_verdadero}, Tipo: {type(es_verdadero)}")
print(f"Es falso: {es_falso}, Tipo: {type(es_falso)}")

